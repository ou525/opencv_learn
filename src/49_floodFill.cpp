#include <opencv2/opencv.hpp>  
#include <opencv2/imgproc/imgproc.hpp>  
using namespace cv;  



//-----------------------------------【main( )函数】--------------------------------------------  
//      描述：控制台应用程序的入口函数，我们的程序从这里开始  
//----------------------------------------------------------------------------------------------- 
int main( )
{    
	Mat src = imread("./data/11.jpg"); 
	imshow("source image",src);
	Rect ccomp;
	floodFill(src, Point(200,200), Scalar(100, 125,155), &ccomp, Scalar(10, 10, 10),Scalar(10, 10, 10));
	imshow("floodFill",src);
	waitKey(0);
	return 0;    
}  